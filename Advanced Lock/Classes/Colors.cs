            /// Text Color
            /// </summary>
            public static Color Light_Gray { get => Color.FromArgb(222, 222, 222); }
            /// <summary>
            /// BTN Color
            /// </summary>
            public static Color Midnight_Blue { get => Color.FromArgb(25, 25, 112); }
        }
    }
